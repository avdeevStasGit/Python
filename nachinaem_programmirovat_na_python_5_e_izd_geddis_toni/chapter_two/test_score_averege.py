# Получить три оценки.
estimation_one = float(input('Введите первую оценку: '))
estimation_two = float(input('Введите вторую оценку: '))
estimation_tree = float(input('Введите третью оценку: '))

# Вычисляем средний балл.
averege_score = (estimation_one + estimation_two + estimation_tree) / 3.0

# Показать средний балл.
print('Средний балл составляет', averege_score)